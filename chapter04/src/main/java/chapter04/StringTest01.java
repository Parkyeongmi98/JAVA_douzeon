package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// C:\temp
		System.out.println("c:\\temp");
		
		// "HELLO
		System.out.println("\"HELLO\"");
		
		// \t : tab
		// \r : carriage return
		// \n : new line(=println)
		// \b : bell
		System.out.print("hello\tworld\n");
		System.out.println("hello\tworld");
		
		// '
		char c = '\'';
		System.out.println(c);
	}

}
